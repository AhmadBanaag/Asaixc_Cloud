package Default;

public class Numbers {
	public static void main(String[] args) {
		
		System.out.println(Math.PI); // Shows the pi digits
		System.out.println(Math.E); // Euler's Number
		
		double num;
		
		num = Math.pow(2, 5); // Creates an exponent for a value
		num = Math.abs(9); // Absolute value of a digit
		num = Math.sqrt(9); // Square root of a value
		num = Math.round(3.89); // Round-off to the nearest whole number
		num = Math.ceil(3.89); // Round up to one
		num = Math.floor(3.89); // Round off to negative one
		num = Math.max(10, 30); // Outputs the maximum value in a condition
		num = Math.min(10, 30); // Shows the minimum value in a condition
		
		
		System.out.println(num);
	}

}
